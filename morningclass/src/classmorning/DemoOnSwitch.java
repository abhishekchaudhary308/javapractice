package classmorning;

public class DemoOnSwitch
{
    public static void main(String[] args)
    {
        int var = 12;
        switch (var)
        {
            case 14 :
                System.out.print("Hello");
                break;
            case 12 :
                System.out.print("Hi");
            default :
                System.out.print("How r u?");
        }
    }
}
